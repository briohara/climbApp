import React, { useState } from "react";
import Avatar from "@material-ui/core/Avatar";
import Button from "@material-ui/core/Button";
import CssBaseline from "@material-ui/core/CssBaseline";
import TextField from "@material-ui/core/TextField";
import Visibility from "@material-ui/icons/Visibility";
import VisibilityOff from "@material-ui/icons/VisibilityOff";
import InputAdornment from "@material-ui/core/InputAdornment";
import IconButton from "@material-ui/core/IconButton";
import Link from "@material-ui/core/Link";
import Grid from "@material-ui/core/Grid";
import Box from "@material-ui/core/Box";
import LockOutlinedIcon from "@material-ui/icons/LockOutlined";
import Typography from "@material-ui/core/Typography";
import { makeStyles } from "@material-ui/core/styles";
import Container from "@material-ui/core/Container";
import { useHistory } from "react-router-dom";
import jwt_Decode from "jwt-decode";

import * as api from "../api/api";
import Copyright from "./Copyright";
import GoogleSignInButton from "./GoogleSignInButton";

const useStyles = makeStyles(theme => ({
  paper: {
    marginTop: theme.spacing(8),
    display: "flex",
    flexDirection: "column",
    alignItems: "center"
  },
  avatar: {
    margin: theme.spacing(1),
    backgroundColor: theme.palette.secondary.main
  },
  form: {
    width: "100%", // Fix IE 11 issue.
    marginTop: theme.spacing(1)
  },
  submit: {
    margin: theme.spacing(3, 0, 2)
  }
}));

const SignIn = props => {
  const history = useHistory();

  const [values, setValues] = useState({
    email: "",
    password: "",
    showPassword: false,
    name: "",
  });

  if (
    localStorage.getItem("JWT") &&
    jwt_Decode(localStorage.getItem("JWT")).exp > new Date().getTime() / 1000
  ) {
    history.push("dashboard");
  }

  const handleSubmit = e => {
    e.preventDefault();

    api
      .signIn({
        email: values.email,
        password: values.password,
      })
      .then(res => {
        localStorage.setItem("JWT", res.data.token);
        localStorage.setItem("name", res.data.name);
        props.setLoggedIn(true);
        history.push("dashboard");
      })
      .catch(err => console.log(err));
  };

  const onGoogleLoginSuccess = googleUser => {
    //Grab id token
    //const id_token = googleUser.getAuthResponse().id_token;

    //verify with backend
    api
      .verifyGoogleUser(googleUser)
      .then(res => {
        api.googleUserSignIn(res.data)
        .then(res => {
          if(res.data.newUser) {
            api.createUser(res.data.newUser)
              .then(res => {
                localStorage.setItem("JWT", res.data.token);
                localStorage.setItem("name", res.data.name);
                //props.setLoggedIn(true);
                history.push("dashboard");
              });
          }

          localStorage.setItem("JWT", res.data.token);
          localStorage.setItem("name", res.data.name);
          //props.setLoggedIn(true);
          history.push("dashboard");
        })
      })
      .catch(err => console.log(err));
  };

  const handleChange = prop => event => {
    setValues({ ...values, [prop]: event.target.value });
  };

  const handleClickShowPassword = () => {
    setValues({ ...values, showPassword: !values.showPassword });
  };

  const handleMouseDownPassword = event => {
    event.preventDefault();
  };

  const classes = useStyles();

  return (
    <Container component="main" maxWidth="xs">
      <CssBaseline />
      <div className={classes.paper}>
        <Avatar className={classes.avatar}>
          <LockOutlinedIcon />
        </Avatar>
        <Typography component="h1" variant="h5">
          Sign in
        </Typography>
        <form className={classes.form} noValidate onSubmit={handleSubmit}>
          <TextField
            variant="outlined"
            margin="normal"
            required
            fullWidth
            id="email"
            label="Email"
            name="email"
            value={values.email}
            onChange={handleChange("email")}
            autoFocus
          />
          <TextField
            variant="outlined"
            margin="normal"
            required
            fullWidth
            name="password"
            label="Password"
            id="password"
            type={values.showPassword ? "text" : "password"}
            value={values.password}
            onChange={handleChange("password")}
            InputProps={{
              endAdornment: (
                <InputAdornment position="end">
                  <IconButton
                    aria-label="toggle password visibility"
                    onClick={handleClickShowPassword}
                    onMouseDown={handleMouseDownPassword}
                  >
                    {values.showPassword ? <Visibility /> : <VisibilityOff />}
                  </IconButton>
                </InputAdornment>
              )
            }}
          />
          <Button
            type="submit"
            fullWidth
            variant="contained"
            color="primary"
            className={classes.submit}
          >
            Sign In
          </Button>
          <Grid container>
            <Grid item xs>
              <Link href="#" variant="body2">
                Forgot password? Go fuck yourself
              </Link>
            </Grid>
            <Grid item>
              <Link href="/signup" variant="body2">
                Don't have an account? Sign Up
              </Link>
            </Grid>
          </Grid>
        </form>
        <div>
          <GoogleSignInButton onSuccess={onGoogleLoginSuccess} />
        </div>
      </div>
      <Box mt={8}>
        <Copyright />
      </Box>
    </Container>
  );
};

export default SignIn;
